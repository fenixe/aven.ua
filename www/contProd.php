<li>➢	Пол фургона сделан из доски, сверху покрытый влагостойкой фанерой.</li>
<li>➢	Наружная обшивка выполнена из оцинкованного листа. Нижняя часть грузовой платформы закрыта оцинкованным листом, соответственно защищающим деревянный настил от воды и грязи. Оцинкованные листы покрыты защитной пленкой, которая удаляется перед началом эксплуатации фургона.</li>

       <a href="img/3.1/3.1.1.jpg"><h2>Хлебный фургон</h2></a>
       <p><em>Сфера применения - доставка хлебобулочных изделий, тортов, других видов кондитерских изделий в лотках при заданном температурном режиме.</em></p>
      <p>Кузов-фургон имеет прямоугольную форму с боковыми одностворчатыми распашными дверьми. Возможны варианты габаритных размеров кузова, количества дверей и лотков по индивидуальному заказу.</p>
      <p>Фургоны оборудуются габаритными огнями и фонарем внутреннего освещения. Устанавливаются на шасси отечественных и импортных производителей.</p>
      <p>Двери фургона расположены с правой стороны вряд(можно и с лева), разделяя кузова на равномерные отсеки в которых располагаются направляющие под лотки.</p>
      <p>Платформа изготовлена из гнутых заводских стальных профилей из низколегированной стали, толщиной 2-5мм, покрытых антикоррозийным грунтом и покрашенных в черный красный или серий цвет.</p>
      <p>Пол изолированный, многослойный, высоко стойкий, укрыт влагостойкий фанерой с антикользящей насечкой.</p>
      <p>В конструкции хлебных фургонов используется термоизолирующий слой. Толщина этого слоя в стандартной комплектации составляет 50 мм, хотя в зависимости от заказа может составлять от 25 мм до 100 мм. В качестве термоизолятора используется пенопласт, пенополиуретан, пенополистирол.</p>
  <p><strong>Стандартная комплектация:</strong></p>
       <ul>
<li>- двустворчатые двери, угол открытия 270 с фиксацией в открытом положении;</li>
<li>- металлическая  лестница;</li>
<li>- внутреннее, габаритное, маркерное освещение;</li>
<li>- металлические, или пластиковые подкрылки, резиновые отбойники;</li>
<li>- внешний каркас - алюминиевый;</li>
<li>- портал под двери - нержавеющая сталь;</li>
<li>- оцинкованная, или нержавеющая фурнитура.</li>
      </ul>
      <p>Конструкция фургона имеет металлический сварной каркас, к которому с помощью само вытяжных алюминиевых клепок крепятся элементы обшивки фургона;</p>
      <p><strong>Существуют различные варианты обшивки:</strong></p>
  <ul>
<li>- стальной оцинкованный лист, толщиной 0,5 × 0,55мм с плакированным покрытием;</li>
<li>- наружная облицовка стеклопластиком:</li>
      <ul>
<li>•    Не деформируется от перепада температур – рабочий режим: -45ºС до +120ºС.</li>
<li>•	Высокая устойчивость к агрессивным средам.</li>
<li>•	Высокое сопротивление к пятнам и различным трещинам.</li>
<li>•	Использование стеклопластика, позволяет на 20% облегчить массу фургонов (по сравнению с металлом), тем самым увеличить полезную грузоподъемность и сэкономить топливные расходы.</li>
<li>•	Термальная устойчивость (низкий коэффициент теплопроводности).</li>
<li>•	Благодаря гигиеническим свойствам стеклопластика – идеальная возможность для производства кузовов-фургонов предназначенных для перевозки  в охлажденном и замороженном виде скоропортящихся пищевых продуктов и медицинских препаратов, требующих специального температурного режима хранения.</li>
<li>•	Краситель добавляются в сырье в процессе производства, поэтому пластик никогда не нуждается в дополнительной покраске или лакировании, легкий монтаж, простой ремонт.</li>
      </ul>
  </ul>

<p>Все платформы хлебных фургонов имеют решетчатую несущую конструкцию, из оригинальных стальных профилей.</p>
<p>Покрытие пола фургона: фанера 18 мм, пропитанная специальным защитным составом Возможно изготовление полового покрытия по индивидуальному заказу.</p>
<p>Крыша фургона собирается из оцинкованных листов на решетчатом каркасе, стыки обрабатываются силиконовым герметикам. На створках дверей по периметру установлен эластичный износостойкий уплотнитель, служащий для предотвращения попадания внутрь автофургона пыли и грязи. Возможно изготовление фургона по индивидуальному заказу.</p>

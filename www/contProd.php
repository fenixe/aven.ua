     <h2>Фургоны промтоварные</h2>
  <p><em><strong>Промтоварные фургоны</strong> предназначены для перевозки грузов, которые требуют защиты от атмосферных осадков и промышленных товаров общего назначения: мебели, техники и другой продукции, не требующей специального температурного режима.</em></p>
  <p>Автофургон имеет прямоугольную форму с задней двухстворчатой распашной дверью. Размеры фургона могут быть различными.</p>
  <p><strong>Заботясь о долговечности наших автофургонов,</strong> мы грунтуем весь каркас фургона и покрываем его в 2 слоя антикоррозионной краской.</p>
  <p>Обшивка крепится к каркасу вытяжными стальными заклепками с шагом 60 мм. Каждая панель обшивки тщательно окрашена специальной полимерной краской до ее монтажа.</p>
  <p><strong>Мы уделяем особое внимание герметизации фургонов.</strong> Зазоры между панелями обшивки и каркасом обрабатываются герметикам до и после склепывания. Между листами обшивки крыши и каркасом прокладывается бутиловая двухсторонняя лента. Наружные стыки обрабатываются водостойким герметикам.</p>
